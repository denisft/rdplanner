import { describe, it, expect } from 'vitest';
import { nextReleaseDay } from './dates';

// 2026-06: 01 пн, 02 вт, 03 ср, 04 чт, 05 пт, 08 пн, 09 вт, 10 ср, 11 чт.
describe('nextReleaseDay — ближайший вт/чт строго после окончания QA', () => {
  it('QA закончилось во вторник → релиз в четверг', () => {
    expect(nextReleaseDay('2026-06-02')).toBe('2026-06-04');
  });

  it('QA закончилось в четверг → релиз в следующий вторник', () => {
    expect(nextReleaseDay('2026-06-04')).toBe('2026-06-09');
  });

  it('QA закончилось в пятницу → релиз во вторник', () => {
    expect(nextReleaseDay('2026-06-05')).toBe('2026-06-09');
  });

  it('QA закончилось в понедельник → релиз во вторник', () => {
    expect(nextReleaseDay('2026-06-01')).toBe('2026-06-02');
  });

  it('релиз всегда строго после дня окончания QA', () => {
    expect(nextReleaseDay('2026-06-09')).toBe('2026-06-11'); // вт → чт
  });
});
